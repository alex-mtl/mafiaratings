<p>Mafia World is a Vancouver-based club for players of &laquo;Mafia&raquo;. &laquo;Mafia&raquo; is a role-playing game,
which is played by 10 people and a Moderator. Among the 10 players, there are 3 Mafiosi, or &laquo;Dark Players&raquo;.
The Mafiosi know who all the Mafiosi are. All the other Players are honest citizens (civilians, or
&laquo;Red Players&raquo;). The goal of the Red Players is to figure out who the Dark Players (Mafiosi) are and
to kill them. The goal of the Dark Players is to kill all the civilians.</p>

<p>The game was invented in 1986 in Russia by Dmitri Davidov, a Psychology student at Moscow University.
It has since become so popular that few students and graduates aren not familiar with it in Russia. In Moscow
there are over 100 nightclubs in which people spend entire nights playing Mafia.</p>

<img src="images/14.jpg" align="left" width="250">
<p>These clubs call themselves &laquo;alternative nightlife&raquo; locations - that is, they provide a type of
nightlife without loud music, dark lounges, or dancing. Although, along with games, 
most of these clubs organize dance parties for members, they also
organize other activities such as trips into nature and group vacations.</p>

<p>&laquo;Mafia&raquo; is an intellectual and psychological game. According to players, it resembles poker, in that it
requires self-control and the ability to influence your opponents, as well as optimal accuracy in calculating
combinations. Mafia is a combination of psychology and theater, the ability to manipulate others' opinions,
and exact mathematical computations.</p>

<p>But don't feel intimidated, anyone can play!</p>

<p>Among known players of &laquo;Mafia&raquo; are Amazon.com founder Jeff Bezos and actress Demi Moore.</p>

