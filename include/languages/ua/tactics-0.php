<p>От шерифа в игре зависит практически все. Это единственный красный игрок, который обладает хоть какими-то точными знаниями. Эти знания он обязан донести до остальных красных игроков. Но делать он это должен в совершенно определенный момент – не раньше и не позже. Понимание этого момента – это по сути понимание игры.</p>

<img src="images/4.jpg" align="left" width="250" class="in-text">
<p>Что же это за момент? Это так называемый «критический раунд». Критическим раундом называется день, когда красные игроки обязаны убить мафиози, иначе проиграют. Например, за столом осталось 7 игроков, из которых трое черных. Если этим днем будет убит красный, игра проиграна. Поэтому шериф обязан объявить себя шерифом и рассказать о своих проверках. Это поможет красным принять верное решение.</p>

<p>К сожалению, черные тоже знают об этом. Поэтому обычно один из них тоже объявляет себя шерифом. Разобраться, кто из шерифов настоящий – это задача красных. </p>

<p>Шериф не должен покидать стол не сказав, что он шериф и не назвав проверок. Даже если его убили ночью. Иначе кто-то из мафиози объявит себя шерифом, получит полное доверие и игру можно считать проигранной.</p>

<p>Вот пример правильной игры шерифа:</p>
<ul>
<li>Шериф играет под номером 4. Мафия играет под номерами 2,7 и 9.</li>
<li>Первой ночью мафия убивает игрока номер 1.</li>
<li>Шериф проверяет игрока номер 3. Он оказывается красным.</li>
<li>Днем убивают игрока 8. Шериф точно не знает, кто он – черный, или красный. Поэтому должен предполагать худшее – что он красный.</li>
<li>Второй ночью мафия убивает игрока номер 5.</li>
<li>Шериф проверяет игрока номер 7. Он оказывается черным.</li>
<li>Наступает утро. В игре остались игроки 2,3,4,6,7,9,10. 7 игроков. Убиты 1,5,8. Шериф может с достаточной точностью сказать, что игроки 1 и 5 убитые ночью – красные. Про игрока номер 8 ничего точно не известно. Поэтому шериф предполагает худший вариант – что он красный. Тогда получается, что за столом 7 человек и трое из них черные. Критический круг. Если сейчас убить красного, будет поражение.</li>
<li>Когда шерифу дают слово, он объявляет себя шерифом и сообщает, что игрок номер 3 – красный, а игрок номер 7 – черный. Теперь, чтобы игра продолжилась красные должны убить номер 7.</li>
</ul>
