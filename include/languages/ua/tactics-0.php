<p>Вот другой пример:</p>
<ul>
<li>Шериф играет под номером 1. Мафия играет под номерами 3,5,10.</li>
<li>Первой ночью мафия убивает игрока номер 4.</li>
<li>Шериф проверяет игрока номер 5. Это черный игрок.</li>
<li>Днем убивают игрока номер 5.</li>
<li>Второй ночью мафия убивает игрока номер 7.</li>
<li>Шериф проверяет игрока номер 9. Это красный игрок.</li>
<li>Наступает утро. В игре остались игроки 1,2,3,6,8,9,10. Семь игроков. Но шериф точно знает, что убитый днем номер 5 был черным. Семь игроков, два мафиози. Это не критический раунд. Если убьют красного игрока, игра продолжится. Должен ли шериф вскрываться? Конечно, нет.</li>
<li>Днем убивают игрока номер 6.</li>
<li>Ночью мафия убивает игрока номер 9.</li>
<li>Шериф проверяет игрока номер 3. Это черный игрок.</li>
<li>Наступает утро. В игре остались игроки 1,2,3,8,10. Пять игроков. Шериф не знает цвет игрока номер 6. Поэтому должен предполагать, что в игре двое черных. Критический круг.</li>
<li>Шериф вскрывается и называет проверки. Теперь красным известны два мафиози – 3 и 5. Остается только вычислить 10-го. Шансы красных на победу очень высоки.</li>
</ul>

<p>Если шериф делает первой ночью проверку и игрок оказывается красным, шериф часто жестами дает ему сигнал. Но так, чтобы другие не видели. Это дает им возможность играть вместе. Часто такой игрок объявляет себя шерифом и рассказывает другим шерифские проверки под видом собственных. Тогда мафия пытается убить ночью фальшивого шерифа вместо настоящего. Это очень выгодно красным игрокам, потому что настоящий шериф выживает и продолжает проверки.</p>
